function BlogCard({ post, featured = false }: { post: typeof posts[0]; featured?: boolean }) {
  const [expanded, setExpanded] = useState(false);

  return (
    <article className="blog-card">
      {/* Image */}
      <div className="relative overflow-hidden" style={{ height: featured ? "280px" : "200px" }}>
        <img
          src={post.image}
          alt={post.title}
          className="w-full h-full object-cover transition-transform duration-500 hover:scale-105"
        />
        <div className="absolute inset-0 bg-gradient-to-t from-[#0D1230] to-transparent opacity-60" />
        <div className="absolute top-4 left-4">
          <span className="badge-urgent">{post.tag}</span>
        </div>
      </div>

      {/* Content */}
      <div className="p-6">
        <div className="flex items-center gap-4 mb-3">
          <span className="text-[#00AEEF] text-xs font-bold font-['Montserrat'] uppercase tracking-wider">{post.category}</span>
          <span className="text-slate-500 text-xs font-['DM_Sans']">·</span>
          <div className="flex items-center gap-1 text-slate-500 text-xs font-['DM_Sans']">
            <Clock size={12} />
            {post.readTime}
          </div>
          <span className="text-slate-500 text-xs font-['DM_Sans'] hidden sm:block">{post.date}</span>
        </div>

        <h2 className={`font-black font-['Montserrat'] text-white mb-3 leading-tight ${featured ? "text-xl md:text-2xl" : "text-lg"}`}>
          {post.title}
        </h2>

        <p className="text-slate-400 text-sm font-['DM_Sans'] leading-relaxed mb-5">
          {post.excerpt}
        </p>

        <button
          onClick={() => setExpanded(!expanded)}
          className="flex items-center gap-2 text-[#00AEEF] font-semibold text-sm font-['DM_Sans'] hover:text-white transition-colors"
        >
          {expanded ? "Show Less" : "Read Full Article"}
          <ArrowRight size={14} className={`transition-transform ${expanded ? "rotate-90" : ""}`} />
        </button>

        {/* Full content */}
        {expanded && (
          <div className="mt-6 pt-6 border-t border-[rgba(0,174,239,0.1)]">
            <div className="prose prose-invert prose-sm max-w-none font-['DM_Sans']">
              {post.content.split('\n').map((line, i) => {
                if (line.startsWith('## ')) {
                  return <h2 key={i} className="text-white font-black font-['Montserrat'] text-lg mt-6 mb-3">{line.replace('## ', '')}</h2>;
                }
                if (line.startsWith('### ')) {
                  return <h3 key={i} className="text-[#00AEEF] font-bold font-['Montserrat'] text-base mt-4 mb-2">{line.replace('### ', '')}</h3>;
                }
                if (line.startsWith('**') && line.endsWith('**')) {
                  return <p key={i} className="text-white font-bold text-sm mt-3 mb-1">{line.replace(/\*\*/g, '')}</p>;
                }
                if (line.startsWith('- ')) {
                  return <li key={i} className="text-slate-400 text-sm ml-4 mb-1">{line.replace('- ', '')}</li>;
                }
                if (line.trim() === '') {
                  return <br key={i} />;
                }
                // Handle inline bold
                const parts = line.split(/\*\*(.*?)\*\*/g);
                if (parts.length > 1) {
                  return (
                    <p key={i} className="text-slate-400 text-sm leading-relaxed mb-2">
                      {parts.map((part, j) => j % 2 === 1 ? <strong key={j} className="text-white">{part}</strong> : part)}
                    </p>
                  );
                }
                return <p key={i} className="text-slate-400 text-sm leading-relaxed mb-2">{line}</p>;
              })}
            </div>
          </div>
        )}
      </div>
    </article>
  );
}

